const selectButtonOne = document.getElementById('select-button-1');
selectButtonOne.addEventListener('click', function() {
    const playerOneName = document.getElementById('player-1').innerText;
    const playerList = document.getElementById("player-list").children;
    if(playerList.length<5){
        const lists = document.createElement("li");
        lists.className = "mb-3";
        const listItem = document.createTextNode(playerOneName);
        lists.appendChild(listItem);
        document.getElementById("player-list").appendChild(lists);
        selectButtonOne.style.pointerEvents = 'none';
        selectButtonOne.style.backgroundColor = 'gray';
    }
    else{
        alert('You can not select more than five players');
    }
});

